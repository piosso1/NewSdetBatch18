package replit;

public class WhileLoop5 {
    public static void main(String[] args) {


        //Using do while loop print numbers from 10 to 20
        //Must not include the number 20 in the output

        int i = 10;
        while (i < 20) {
            System.out.println(i);

            i=i+1;
        }
    }
}
