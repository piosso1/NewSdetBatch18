package replit;

public class WhileLoop4 {
    public static void main(String[] args) {

        //Step 1: Initialize the i value as int i=7.
        //Step 2: Write a program to print a sequence of numbers starting from 7 to 98.


        int i = 7;
        while (i < 99) {
            System.out.println(i);

            i = i + 7;
        }
    }
}
