package replit;

public class Arrays {
    public static void main(String[] args) {

        //Write a program that creates an array of integers and stores the
        //following values: 45, 78, 12,  67, 55 and then prints all array values.

        int [] a={45, 78, 12, 67, 55};

        for (int i=0; i<a.length;i++) {
            System.out.println(a[i]+" ");
        }
    }
}
