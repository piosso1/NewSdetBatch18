package replit;

public class ForLoop5 {
    public static void main(String[] args) {

        //Write a program that uses for loop to prints multiplication of  number 3 from 1 to 10

        for (int i=3; i<=3; i++) {

            for (int j=1; j<=10; j++) {
                System.out.println(i+"*"+j+"="+(i*j));
            }
        }
    }
}