package replit;

public class DoWhileLoop8 {
    public static void main(String[] args) {

        //Create a for loop that prints out numbers from 0 to 12
        //Must not include the number 12 in the output


        int i = 0;

        do {
            System.out.println(i);
            i=i+1;
        }while (i < 12);
    }
}
