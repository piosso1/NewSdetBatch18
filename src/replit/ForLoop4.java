package replit;

public class ForLoop4 {
    public static void main(String[] args) {

        //Write a program that prints the count down from 10 up to 1 and then says "Happy New Year!".

        for (int i=10; i>0; i--) {
            System.out.println(i);
        }
        System.out.println("Happy New Year!");
    }
}
