package class2;

public class E6dConcat {

    public static void main(String[] args) {

        char a = '*';
        char c = '#';
        char[] charArray = {'*', '*'};
        char[] charArray3 = {'*', '*', '*'};
        char[] charArray4 = {'#', '#'};

        System.out.println(a);
        System.out.println(charArray);
        System.out.println(charArray3);
        System.out.println(charArray4);
        System.out.println(c);
    }
}
