package Class6;

public class T13For {
    public static void main(String[] args) {

        for (int i = 20; i >= 2; i = i - 2) {
            System.out.print(i + " ");
        }

        int i = 20;

        while (i >= 2) {
            System.out.println(i + " ");
            i=i-2;
        }
    }
}