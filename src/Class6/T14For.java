package Class6;

public class T14For {
    public static void main(String[] args) {

        for (int i = 19; i<=49; i = i + 2) {
            System.out.print(i + " ");
        }

        int i = 19;

        while (i <= 49) {
            System.out.print(i + " ");
            i = i + 2;
        }
    }
}
